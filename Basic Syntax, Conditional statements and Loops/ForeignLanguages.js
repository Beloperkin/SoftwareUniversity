function foreignLanguages (country)
{
    switch(country)
    {
        case 'USA':
            console.log('English');
            break;
            
        case 'England':
            console.log('English');
            break;

        case 'Spain':
        
            console.log('Spanish');
            break;
        
        case 'Argentina':
        
            console.log('Spanish');
            break;

        case 'Mexico':
        
            console.log('Spanish');
            break;

        default:
        
            console.log('Unknown');
            break;
        
        
    }
}

foreignLanguages('USA');
foreignLanguages('England');
foreignLanguages('Mexico');
foreignLanguages('Argentina');
foreignLanguages('Spain');
foreignLanguages('Bulgaria');